def bank(a, years):
    """Рассчитывает конечную сумму вклада с капитализацией процентов.

    Каждый год вклад увеличивается на 10%, и проценты начисляются на увеличенную сумму.

    Args:
        a (float): Начальная сумма вклада в рублях.
        years (int): Срок вклада в годах.

    Returns:
        float: Конечная сумма, которая будет на счету пользователя после указанного срока.

    Raises:
        ValueError: Если начальная сумма вклада a или количество лет years меньше или равны нулю.
    """
    # Проверяем, что начальная сумма вклада положительна
    if a <= 0:
        raise ValueError("Начальная сумма вклада должна быть положительным числом.")

    # Проверяем, что срок вклада положительный
    if years <= 0:
        raise ValueError("Срок вклада (years) должен быть положительным числом.")

    # Годовая процентная ставка 10% выражается как 0.10
    rate = 0.10

    # Используем формулу сложных процентов:
    # конечная сумма = a * (1 + rate) ** years
    final_amount = a * (1 + rate) ** years

    return final_amount


# Пример использования функции
if __name__ == "__main__":
    try:
        initial_amount = 1000  # Начальная сумма вклада в рублях
        duration = 5  # Срок вклада в годах
        result = bank(initial_amount, duration)
        print(f"Сумма на счету через {duration} лет: {result:.2f} рублей")
    except ValueError as e:
        print("Ошибка:", e)
